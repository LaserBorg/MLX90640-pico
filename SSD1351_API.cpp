void SSD1351_init(void)
{
    SSD1351_write_command(SSD1351_CMD_COMMANDLOCK, "\x12", 1);
    SSD1351_write_command(SSD1351_CMD_COMMANDLOCK, "\xB1", 1);
    SSD1351_write_command(SSD1351_CMD_DISPLAYOFF, NULL, 0);
    SSD1351_write_command(SSD1351_CMD_CLOCKDIV, "\xF1", 1);
    SSD1351_write_command(SSD1351_CMD_MUXRATIO, "\x7F", 1);
    SSD1351_write_command(SSD1351_CMD_SETREMAP, "\x74", 1);
    SSD1351_write_command(SSD1351_CMD_STARTLINE, "\x00", 1);
    SSD1351_write_command(SSD1351_CMD_DISPLAYOFFSET, "\x00", 1);
    SSD1351_write_command(SSD1351_CMD_SETGPIO, "\x00", 1);
    SSD1351_write_command(SSD1351_CMD_FUNCTIONSELECT, "\x01", 1);
    SSD1351_write_command(SSD1351_CMD_PRECHARGE, "\x32", 1);
    SSD1351_write_command(SSD1351_CMD_PRECHARGELEVEL, "\x1F", 1);
    SSD1351_write_command(SSD1351_CMD_VCOMH, "\x05", 1);
    SSD1351_write_command(SSD1351_CMD_NORMALDISPLAY, NULL, 0);
    SSD1351_write_command(SSD1351_CMD_CONTRASTMASTER, "\x0A", 1);
    SSD1351_write_command(SSD1351_CMD_CONTRASTABC, "\xFF\xFF\xFF", 3);
    SSD1351_write_command(SSD1351_CMD_SETVSL, "\xA0\xB5\x55", 3);
    SSD1351_write_command(SSD1351_CMD_PRECHARGE2, "\x01", 1);
    SSD1351_write_command(SSD1351_CMD_DISPLAYON, NULL, 0);
}

void SSD1351_clear(void)
{
    memset(displayRAM.byte, 0, DRAM_SIZE_8);
}

void SSD1351_pixel(int16_t x, int16_t y, uint16_t color)
{
    displayRAM.halfw[y * DISPLAY_WIDTH + x] = color;
}

void SSD1351_fillrect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color)
{
    for (int n = 0; n < h; n++) {
        int base = (y+n) * DISPLAY_WIDTH;
        for (int m = 0; m < w; m++) {
            displayRAM.halfw[base+x+m] = color;
        }
    }
}

int SSD1351_char(int16_t x, int16_t y, int8_t c, uint16_t color) {
    if (c < 32 || c > 126) {
        return 0;
    }
    int idx = (c-32) * 5;
    int w = 5; 
    for (int n = 0; n < w; n++) { // columns
        uint8_t column = fixedwithfont[idx++];
        for (int m = 0; m < 8; m++) {   // rows
            if (column & 0x01) {
                SSD1351_pixel(x+n, y+m, color);
            }
            column >>= 1;
        }
    }
    return w;
}

int SSD1351_charwidth(int8_t c) {
    return c < 32 || c > 126 ? 0 : 5;
}

int SSD1351_text(int16_t x, int16_t y, char *s, uint16_t color) {
    while (*s) {
        x += SSD1351_char(x, y, *s++, color) + 1;
    }
    return x;
}

int SSD1351_textwidth(char *s) {
    int x = 0;
    while (*s) {
        x += SSD1351_charwidth(*s++) + 1;
    }
    return x;
}

void SSD1351_update(void)
{
    SSD1351_write_command(SSD1351_CMD_SETCOLUMN, "\x00\x7F", 2);
    SSD1351_write_command(SSD1351_CMD_SETROW, "\x00\x7F", 2);
    SSD1351_write_command(SSD1351_CMD_WRITERAM, (const char*) displayRAM.byte, DRAM_SIZE_8);
}

uint16_t SSD1351_color(uint8_t r, uint8_t g, uint8_t b) {
    uint16_t x = (r & 0xf8) << 8 | (g & 0xfc) << 3 | b >> 3;
    return (x & 0xff00) >> 8 | (x & 0x00ff) << 8;
}